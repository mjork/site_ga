	<h2>La FNEC ou comment concilier ingénierie et loisirs</h2>
	<p class="details">Released on 20 feb 2011, Journal des Grandes Ecoles</p>
	<p class="subtitle">Les 2 et 3 avril derniers, l'association BEST (Board of European Students of Technology) a organisé la finale française d'une compétition d'ingénierie européenne, la French National Engineering Competition. Cinq écoles d'ingénieurs, Centrale Paris, ENSTA Paritech, Supélec, Arts et Métiers ParisTech, Grenoble INP, se sont affrontées autour de deux épreuves : un Case Study et un Team Design.</p>
