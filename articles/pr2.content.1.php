<div class="span-13 append-1">
	<p> Le Case Study (ou étude de cas) a été réalisée avec l'aide de membres de l'association Sol'R, dont le
		projet réel est de concevoir, construire et faire voler le premier dirigeable à énergie solaire. Le Case
		Study a donc consisté en la préparation d'un vol en ballon au-dessus de l'Atlantique, le ballon n'étant
		alimenté que par de l'énergie solaire. Les équipes avaient pour mission de déterminer la trajectoire,
		les dimensions et les propriétés du ballon et de ses équipements. Ils ont aussi été placés face à des
		problèmes avec leur partenaire. Ils ont pu calculer un budget, organiser les différentes tâches de la
		concrétisation de la traversée et remotiver leur équipe pour tenir la durée du projet. </p>
	<p> Le Team Design consiste à résoudre un problème donné en équipes, en un temps limité et avec
		une quantité réduite de matériel, donc un cahier des charges qui sort de l'ordinaire. Le sujet était
		d'allumer une diode après une chaîne d'action. En utilisant l'effet domino, les équipes devaient faire
		clignoter la diode de manière à écrire en morse le nom de notre association, BEST (ce qui donne -…/./
		…/-). Ils avaient aussi des objets à utiliser obligatoirement pour corser l'épreuve. Cette épreuve tenait
		compte des idées mais aussi de la réussite du dispositif lors de la présentation. </p>
	<p> Les deux épreuves ont été évaluées par une présentation en anglais, langue usuelle et officielle dans
		les différents évènements de BEST. </p>
	<p> Les épreuves se sont déroulées sur deux jours suivis d'une remise des prix. Chaque équipe
		participante est repartie avec un lot. Au final, c'est l'équipe de Grenoble INP qui a remporté
		l'épreuve de l'étude de cas et l'équipe de Supélec qui a gagné le maximum de points au team design.
		Les deux équipes gagnantes iront donc à la grande finale européenne, qui sera organisée à Cluj-
		Napoca en Roumanie cet été. L'association BEST international leur financera leur voyage et leur
		semaine sur place lorsqu'ils défendront les couleurs françaises. </p>
	<p> L'évènement a été accueilli par BEST Supélec et préparé en collaboration par les huit antennes
		françaises : ENSTA, Supélec, Centrale, ENSAM, Polytechnique, INSA Lyon, INP Lorraine (Nancy) et
		Grenoble INP. </p>
	<p> L'Union Européenne, via le programme Youth in Action, qui soutient les actions culturelles et
		d'échange entre les jeunes européens, ainsi que Cisco Systems ont participé au financement de cet
		événement. </p>
	<p> Voici pour finir quelques phrases des gagnants au sujet des deux jours. </p>
	<p class="quote"> « On ne pensait pas gagner en partant à Paris, on venait pour découvrir Supélec et s'amuser. <br />
