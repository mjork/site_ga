		<h2>Our Structure</h2>
		<p>The basis of our organisation is formed by our 91 <a href="http://best.eu.org/aboutBEST/structure/lbgList.jsp">Local BEST   Groups</a> spread in 30 European countries. Each of these groups have   their own local boards and organise local and international BEST activities.</p>
		<p>Next to the local groups, we have a well developed international   structure. The main tasks of the International Board are global coordination of   BEST and its bodies and programmes, execution of General Meetings decisions, promotion   of aims and purpose of BEST and support for quality assurance of all   BEST activities.</p>
		<p>BEST members regularly meet for <a href="http://best.eu.org/aboutBEST/structure/internalEvents.jsp">internal events</a> where they can work on BEST-specific issues, receive training and strengthen their ties.</p>
